# Открываем файл с данными о заболеваемости в разных регионах
with open('data.txt', 'r') as file:
    # Считываем содержимое файла
    content = file.read()

# Разбиваем содержимое файла на строки
lines = content.split('\n')

# Создаем словарь для хранения количества заболевших в каждом регионе
regions = {}

# Проходим по каждой строке файла
for line in lines:
    # Разбиваем строку на регион и количество заболевших
    region, cases = line.split(':')
    # Преобразуем количество заболевших в целое число
    cases = int(cases)
    # Добавляем регион и количество заболевших в словарь
    regions[region] = cases

# Находим максимальное количество заболевших
max_cases = max(regions.values())

# Создаем список для хранения регионов с максимальным количеством заболевших
Maxregions = []

# Проходим по каждому региону и количеству заболевших
for region, cases in regions.items():
    # Если количество заболевших равно максимальному, добавляем регион в список
    if cases == max_cases:
        Maxregions.append(region)

# Выводим список регионов с максимальным количеством заболевших
print("Регионы с наибольшим количеством заболевших:")
for region in Maxregions:
    print(region)
